package m01_printing_escape_sequences;

/*
Write a main method and print statements to display the paths as below:

Windows path example: C:\Users\Username\Documents\file.txt
macOS path example: /Users/Username/Documents/file.txt
Note: Use one statement for each definition part and another statement for displaying the path.
 */

public class PrintPaths {
    public static void main(String[] args) {
        System.out.print("Windows path example: ");
        System.out.println("C:\\Users\\Username\\Documents\\file.txt");
        System.out.print("macOS path example: ");
        System.out.println("/Users/Username/Documents/file.txt");
    }
}
